                        <h2><font color=red>「年収の壁」への当面の対応・支援強化パッケージの詳細が発表されました</font></h2>
                        <p>　厚生労働省は、労働者が社会保険料の負担による手取り収入の減少を避けるために就業調整をする、いわゆる「年収の壁」問題への当面の対策として、支援強化パッケージの詳細を発表しました。パッケージは、10月から順次実施されます。</p>
                        <br>
                        <h3><font color=blue>◆106万円の壁への対応・キャリアアップ助成金のコースの新設</font></h3>
                        <p>　短時間労働者を新たに被保険者とする際に、労働者の収入を増加させる取組みを行った事業主は、一定期間助成（労働者１人当たり最大50万円）を受けることができます。<br>
                        助成対象の取組みには、賃上げや所定労働時間の延長のほか、保険料負担に伴う手取り収入の減少分に相当する手当（社会保険適用促進手当）の支給も含まれます。<br><br>
                        ・社会保険適用促進手当の標準報酬算定除外<br><br>
                        　事業主は、当該労働者に対し、給与・賞与とは別に「社会保険適用促進手当」を支給できます。また、労使双方の保険料負担を軽減する観点から、社会保険適用促進手当については、労働者負担分の保険料相当額を上限として、最大２年間、標準報酬月額・標準賞与額の算定に考慮しません。</p>
                        <br>
                        <h3><font color=blue>◆130万円の壁への対応</font></h3>                  
                        <p>・事業主の証明による被扶養者認定の円滑化<br><br>
                        　直近の年間収入が、被扶養者の認定の要件である130万円を超える見込みとなった場合、過去の課税証明書、給与明細書、雇用契約書等に加えて、人手不足による労働時間延長等に伴う一時的な収入変動である旨の事業主の証明を添付することで、直ちに被扶養者認定を取り消されることはなく、総合的に将来収入の見込み額から判断し、迅速な認定を受けることができます。</p>
                        <br>
                        <h3><font color=blue>◆配偶者手当への対応</font></h3>
                        <p>・企業の配偶者手当の見直し促進<br><br>
                        　令和６年春の賃金見直しに向けた労使の話し合いの中で、中小企業においても配偶者手当の見直しが進むよう、見直しの手順をフローチャートで示す等わかりやすい資料を作成・公表します。また、各地域で開催されるセミナーで説明、中小企業団体等を通じての周知活動を行います。<br><br>
