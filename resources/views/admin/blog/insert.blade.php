                        <h2><font color=red>仕事と育児の両立支援制度に関する意識・実態調査(連合の調査から)</font></h2>
                        <br>
                        <h3><font color=blue>◆調査の概要</font></h3><br>
                        <p>　仕事と育児の両立支援制度に対する意識や実態を把握するために、日本労働組合総連合会（連合）が実施する「仕事と育児の両立支援制度に関する意識・実態調査2023」の結果が公表されました。小学生以下の子を持つ20歳～59歳の働く男女1,000名が回答したこの調査は、仕事と育児の両立のために何が求められているのか、様々なヒントを与えてくれます。</p>
                        <br><br>
                        <h3><font color=red>◆調査結果のポイント</font></h3>  <br>      
                        <p>仕事と育児の両立のために利用したことがある両立支援制度」を問う質問では、育児休業（41.9％）や短時間勤務（16.3％）が挙げられる一方で、「利用したことのある制度はない」は47.8％、男性では58.4％にのぼります。その理由の１位は「利用できる職場環境ではなかった」というものです。なぜそのように思ったのか、という質問には「代替要員がいなかった」（39.6％）が最も多く、「職場の理解が低かった」（33.7％）、「言い出しにくかった」（26.2％）、「自分にしかできない業務を担っていた」（20.3％）が続きました。<br>
                        代替要員がいない、理解が低いという職場では、両立支援制度を利用しづらいという現状がうかがえます。こうした状況は採用活動においても不利に働き、いっそうの人手不足を生み出す負のスパイラルへと繋がってしまいます。両立支援を必要とする従業員のみならず、職場全体で考えるべき問題です。属人化している業務はないか、理解のない言動は見られないかなど、職場全体で両立支援について考えていきたいですね。</p>
